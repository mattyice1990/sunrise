// build-blog-index.mjs — render static, crawlable post cards into blog/index.html
// from blog/blog-posts.json. Run after adding/removing a post:
//     node scripts/build-blog-index.mjs
// blog-loader.js enhances these on JS load; the static cards guarantee every
// post has an incoming internal link (no orphan pages) for non-JS crawlers.

import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");
const INDEX = join(ROOT, "blog", "index.html");
const POSTS = join(ROOT, "blog", "blog-posts.json");

const START = "<!-- BLOG_CARDS_START -->";
const END = "<!-- BLOG_CARDS_END -->";

function esc(s) {
  return String(s == null ? "" : s)
    .replace(/&/g, "&amp;").replace(/</g, "&lt;").replace(/>/g, "&gt;").replace(/"/g, "&quot;");
}

function formatDate(dateString) {
  const [y, m, d] = String(dateString).split("-").map(Number);
  if (!y || !m || !d) return esc(dateString);
  const date = new Date(y, m - 1, d);
  return date.toLocaleDateString("en-US", { year: "numeric", month: "short", day: "numeric" });
}

// Mirrors blog-loader.js createArticleCard, but links to /blog/<slug> (no trailing
// slash → direct 200, no redirect hop) and uses escaped, static markup.
function card(post) {
  const href = `/blog/${esc(post.slug)}`;
  return `                <article style="background: white; border-radius: 12px; overflow: hidden; box-shadow: 0 4px 20px rgba(0,0,0,0.1);">
                    <a href="${href}" style="text-decoration: none; color: inherit; display: block;">
                        <div style="position: relative; overflow: hidden; height: 220px;">
                            <img src="${esc(post.image)}" alt="${esc(post.imageAlt || post.title)}" loading="lazy" style="width: 100%; height: 100%; object-fit: cover;">
                            <div style="position: absolute; top: 15px; left: 15px; background: #F5A623; color: white; padding: 5px 15px; border-radius: 20px; font-family: 'Oswald', sans-serif; font-size: 12px; font-weight: 600; text-transform: uppercase; letter-spacing: 0.5px;">${esc(post.category)}</div>
                        </div>
                        <div style="padding: 30px;">
                            <h3 style="font-family: 'Bebas Neue', sans-serif; font-size: 28px; color: #1A1A1A; margin-bottom: 12px; text-transform: uppercase; letter-spacing: 1px; line-height: 1.2;">${esc(post.title)}</h3>
                            <p style="color: #999; font-size: 13px; margin-bottom: 15px; font-family: 'Oswald', sans-serif; text-transform: uppercase; letter-spacing: 0.5px;">${formatDate(post.date)} · ${esc(post.readTime)}</p>
                            <p style="font-size: 16px; line-height: 1.7; color: #666; margin-bottom: 20px;">${esc(post.excerpt)}</p>
                            <span style="font-family: 'Oswald', sans-serif; font-size: 14px; font-weight: 600; text-transform: uppercase; letter-spacing: 0.5px; color: #F5A623; display: inline-flex; align-items: center; gap: 8px;">Read Article <span style="font-size: 16px;">→</span></span>
                        </div>
                    </a>
                </article>`;
}

const posts = JSON.parse(readFileSync(POSTS, "utf8"));
posts.sort((a, b) => new Date(b.date) - new Date(a.date));

let html = readFileSync(INDEX, "utf8");
const s = html.indexOf(START);
const e = html.indexOf(END);
if (s === -1 || e === -1 || e < s) {
  console.error("Markers not found in blog/index.html — aborting.");
  process.exit(1);
}

const block =
  START +
  "\n                <!-- Static, crawlable post links (generated by scripts/build-blog-index.mjs). Do not edit by hand. -->\n" +
  posts.map(card).join("\n") +
  "\n                " +
  END;

html = html.slice(0, s) + block + html.slice(e + END.length);
writeFileSync(INDEX, html);
console.log(`build-blog-index: wrote ${posts.length} static post cards into blog/index.html`);
